'''функция для нахождения площади прямоугольника'''
def area(a, b):
    '''принимает два числа a и b, например a = 3, b = 4'''
    return a * b
'''возвращает произведение поступивших чисел, возвращаемое значение 12'''

'''функция для нахождения периметра прямоугольника'''
def perimeter(a, b):
    '''принимает два числа a и b, например a = 3, b = 4'''
    return (a + b) * 2
'''возвращает произведение суммы чисел a и b на , возвращаемое значение 14'''
