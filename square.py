'''функция для нахождения площади квадрата'''
def area(a):
    '''принимает число а, например a = 3'''
    return a * a
'''возвращает квадрат числа a, возвращаемое значение 9'''

'''функция для нахождения перимитра квадрата'''
def perimeter(a):
    '''принимает число а, например a = 3'''
    return 4 * a
'''возвращает произведения числа a на 4, возвращаемое значение 12'''
