'''функция для нахождения площади треугольника'''
def area(a, h):
    '''принимает два числа a и h, например a = 3, h = 4'''
    return a * h / 2
'''возвращет частное произведения чисел a и h на два, возвращаемое значение 6'''

'''функция для нахождения периметра треугольника'''
def perimeter(a, b, c):
    '''принимает три числа a, b, c, например a = 3, b = 4, c = 5'''
    return a + b + c
'''возвращает сумму чисел a, b, c, возвращаемое значение 12'''
